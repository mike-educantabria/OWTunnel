  Future<void> _register() async {
    setState(() => _isLoading = true);
    try {
      final email = _model.textController1?.text.trim() ?? '';
      final password = _model.textController2?.text ?? '';
      final confirmPassword = _model.textController3?.text ?? '';

      if (email.isEmpty || password.isEmpty || confirmPassword.isEmpty) {
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(content: Text('Please fill in all fields')),
        );
        return;
      }
      if (password != confirmPassword) {
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(content: Text('Passwords do not match')),
        );
        return;
      }

      final url = Uri.parse('http://localhost:8080/api/v1/auth/register');
      final response = await http.post(
        url,
        headers: {'Content-Type': 'application/json'},
        body: jsonEncode({'email': email, 'password': password}),
      );

      // Accept 200 or 201 as success
      if (response.statusCode == 200 || response.statusCode == 201) {
        final data = jsonDecode(response.body);
        if (data['token'] != null) {
          ScaffoldMessenger.of(context).showSnackBar(
            const SnackBar(
                content: Text('Registration successful, please sign in')),
          );
          if (!mounted) return;
          context.go('/loginPage');
          return;
        }
      }

      // Try to detect duplicate email error
      String errorMsg = 'Registration failed: ${response.body}';
      if (response.body.contains('Duplicate entry') ||
          response.body.contains('email_unique')) {
        errorMsg =
            'This email is already registered. Please sign in or use another email.';
      }
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text(errorMsg)),
      );
    } catch (e) {
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text('Connection error: $e')),
      );
    } finally {
      if (mounted) setState(() => _isLoading = false);
    }
  }

